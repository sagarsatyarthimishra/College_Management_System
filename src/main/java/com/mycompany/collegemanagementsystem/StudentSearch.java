    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String roll = input.getText().trim();
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        if (roll.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Roll Number");
        }
        int Roll = Integer.parseInt(roll);
        try {
            model.setRowCount(0);
            //Load Driver
            Class.forName("com.mysql.cj.jdbc.Driver");

            //Connect JDBC Connector
            //Create Connection
            Connection con = DriverManager.getConnection(url, user, password);

            //create query
            String Query = "SELECT * FROM student WHERE rollno = ?";

            //Create Statement
            PreparedStatement ps = con.prepareStatement(Query);

            //Initialize value
            ps.setInt(1, Roll);

            //Excecute Query
            ResultSet rs = ps.executeQuery();

            boolean hasResult = false;
            while (rs.next()) {
                hasResult = true;
                int rollno = rs.getInt(1);
                String RollNo = Integer.toString(rollno);
                String name = rs.getString(2);
                int year = rs.getInt(3);
                String Year = Integer.toString(year);
                String course = rs.getString(4);
                String branch = rs.getString(5);
                String row[] = {RollNo, name, Year, course, branch};
                model.addRow(row);
            }
            if (!hasResult) {
                JOptionPane.showMessageDialog(this, "Please Enter Valid Roll Number");
            }

            //Close connection 
            con.close();

            input.setText("");
            System.out.println("Query Execute Successfull!!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton5ActionPerformed

