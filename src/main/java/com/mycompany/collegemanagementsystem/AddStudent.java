    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String RollNoInput = RollNo.getText();
        String NameInput = Name.getText();
        String YearInput = Year.getText();
        String CourseInput = Course.getText();
        String BranchInput = Branch.getText();
        if (RollNoInput.isEmpty() || NameInput.isEmpty() || YearInput.isEmpty() || CourseInput.isEmpty() || BranchInput.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter the Details in all required field");
        }
        int roll = Integer.parseInt(RollNoInput);
        int Yr = Integer.parseInt(YearInput);
        try {
            //Load Driver
            Class.forName("com.mysql.cj.jdbc.Driver");

            //Create Connection 
            Connection con = DriverManager.getConnection(url, user, password);

            //Create Query
            String Query = "INSERT INTO student VALUES( ?,  ?,  ?,  ?,  ?)";

            //Create Statement
            PreparedStatement ps = con.prepareStatement(Query);
            ps.setInt(1, roll);
            ps.setString(2, NameInput);
            ps.setInt(3, Yr);
            ps.setString(4, CourseInput);
            ps.setString(5, BranchInput);
            int count = ps.executeUpdate();
            if (count > 0) {
                JOptionPane.showMessageDialog(this, "Successfully Inserted!");
            } else {
                JOptionPane.showMessageDialog(this, "Insertion Failed!");
            }
            RollNo.setText("");
            Name.setText("");
            Year.setText("");
            Course.setText("");
            Branch.setText("");
            
            //close connection
            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

