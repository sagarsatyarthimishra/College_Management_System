    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String roll = input.getText().trim();
        //Create Table
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        if (roll.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Roll Number");
        }
        int Roll = Integer.parseInt(roll);
        try {
            model.setRowCount(0);
            //Load Driver
            Class.forName("com.mysql.cj.jdbc.Driver");
            //Setup JDBC connection 

            //Create connection
            Connection con = DriverManager.getConnection(url, user, password);

            //Create Query
            String Query = "DELETE FROM student WHERE rollno = ?";
            
            PreparedStatement ps = con.prepareStatement(Query);
            ps.setInt(1, Roll);

            // Execute delete and get number of affected rows
            int rowsAffected = ps.executeUpdate();
            
            if (rowsAffected > 0) {
                JOptionPane.showMessageDialog(this, "Student with roll number " + Roll + " deleted successfully.");
            } else {
                JOptionPane.showMessageDialog(this, "No student found with roll number " + Roll);
            }
            
            input.setText("");
            //close connection
            con.close();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String roll = input.getText().trim();
        //Create Table
        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        if (roll.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Roll Number");
        }
        int Roll = Integer.parseInt(roll);
        try {
            model.setRowCount(0);
            //Load Driver
            Class.forName("com.mysql.cj.jdbc.Driver");
            //Setup JDBC connection 

            //Create connection
            Connection con = DriverManager.getConnection(url, user, password);

            //Create Query
            String Query = "SELECT * FROM student WHERE rollno = ?";

            //Create Statement
            PreparedStatement ps = con.prepareStatement(Query);
            ps.setInt(1, Roll);
            //Execute Query
            ResultSet rs = ps.executeQuery();
            
            boolean hasResult = false;
            
            while (rs.next()) {
                hasResult = true;
                int rollno = rs.getInt(1);
                String RollNo = Integer.toString(rollno);
                String name = rs.getString("name");
                int year = rs.getInt(3);
                String Year = Integer.toString(year);
                String course = rs.getString(4);
                String branch = rs.getString(5);
                String row[] = {RollNo, name, Year, course, branch};
                model.addRow(row);
                input.setText("");
            }
            
            if (!hasResult) {
                JOptionPane.showMessageDialog(this, "No Data Found");
            }
            //close connection
            con.close();
            
            System.out.print("Execute Query Successfull!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

